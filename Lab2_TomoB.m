clear all;
close all;
x = [0.1:1/22:1];
d = (1 + 0.6*sin(2*pi*x/0.7) + 0.3*sin(2*pi*x))/2;
w11_1 = randn(1)*0.1;
w12_1 = randn(1)*0.1;
w13_1 = randn(1)*0.1;
w14_1 = randn(1)*0.1;
w15_1 = randn(1)*0.1;
b1_1 = randn(1)*0.1;
b2_1 = randn(1)*0.1;
b3_1 = randn(1)*0.1;
b4_1 = randn(1)*0.1;
b5_1 = randn(1)*0.1;
w11_2 = randn(1)*0.1;
w12_2 = randn(1)*0.1;
w13_2 = randn(1)*0.1;
w14_2 = randn(1)*0.1;
w15_2 = randn(1)*0.1;
b1_2 = randn(1)*0.1;
n = 0.55;

for i = 1:150000
    for a=1:length(x)
        v1_1 = x(a)*w11_1 + b1_1;
        v2_1 = x(a)*w12_1 + b2_1;
        v3_1 = x(a)*w13_1 + b3_1;
        v4_1 = x(a)*w14_1 + b4_1;
        v5_1 = x(a)*w15_1 + b5_1;

        y1_1 = 1/(1+exp(-v1_1));
        y2_1 = 1/(1+exp(-v2_1));
        y3_1 = 1/(1+exp(-v3_1));
        y4_1 = 1/(1+exp(-v4_1));
        y5_1 = 1/(1+exp(-v5_1));

        v1_2 = y1_1*w11_2+y2_1*w12_2+y3_1*w13_2+y4_1*w14_2+y5_1*w15_2 + b1_2;
        y1_2 = tanh(v1_2);
        
        y = y1_2;
        e = d(a) - y;   
        
        de1_2 = (1-(tanh(v1_2))^2)*e;
        de1_1 = y1_1*(1-y1_1)*de1_2*w11_2;
        de2_1 = y2_1*(1-y2_1)*de1_2*w12_2;
        de3_1 = y3_1*(1-y3_1)*de1_2*w13_2;
        de4_1 = y4_1*(1-y4_1)*de1_2*w14_2;
        de5_1 = y5_1*(1-y5_1)*de1_2*w15_2;

        w11_2 = w11_2 + n*de1_2*y1_1;
        w12_2 = w12_2 + n*de1_2*y2_1;
        w13_2 = w13_2 + n*de1_2*y3_1;
        w14_2 = w14_2 + n*de1_2*y4_1;
        w15_2 = w15_2 + n*de1_2*y5_1;
        b1_2 = b1_2 + n*de1_2;

        w11_1 = w11_1 + n*de1_1*x(a);
        w12_1 = w12_1 + n*de2_1*x(a);
        w13_1 = w13_1 + n*de3_1*x(a);
        w14_1 = w14_1 + n*de4_1*x(a);
        w15_1 = w15_1 + n*de5_1*x(a);
        b1_1 = b1_1 + n*de1_1;
        b2_1 = b2_1 + n*de2_1;
        b3_1 = b3_1 + n*de3_1;
        b4_1 = b4_1 + n*de4_1;
        b5_1 = b5_1 + n*de5_1;
    end
end

x2 = [0.1:1/220:1];
Y = zeros(1, length(d));

for a=1:length(x2)

        v1_1 = x2(a)*w11_1 + b1_1;
        v2_1 = x2(a)*w12_1 + b2_1;
        v3_1 = x2(a)*w13_1 + b3_1;
        v4_1 = x2(a)*w14_1 + b4_1;
        v5_1 = x2(a)*w15_1 + b5_1;

        y1_1 = 1/(1+exp(-v1_1));
        y2_1 = 1/(1+exp(-v2_1));
        y3_1 = 1/(1+exp(-v3_1));
        y4_1 = 1/(1+exp(-v4_1));
        y5_1 = 1/(1+exp(-v5_1));

        v1_2 = y1_1*w11_2+y2_1*w12_2+y3_1*w13_2+y4_1*w14_2+y5_1*w15_2 + b1_2;
 
        y1_2 = tanh(v1_2);
        y = y1_2;
        Y(a) = y;
end

plot (x,d,x2,Y,'^');










